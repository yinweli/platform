void AvoidAllocMemoryAbnormal()
{
#ifdef _DEBUG
	long lRequest;
	void *p = malloc(10);

	_CrtIsMemoryBlock(p, 10, &lRequest, nullptr, nullptr);
	free(p);
	_CrtSetBreakAlloc((lRequest > -1) ? LONG_MIN / 2 : LONG_MAX / 2);
#endif
}
